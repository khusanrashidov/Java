public class JavaTutorial {
    public static void main(String[] args) {
        //difference between print and print'ln'
        System.out.println("Sentence A");
        System.out.println("Sentence B");
        /*"println" states that the next statement will be
        printed in the next line, however just "print" there
        won't be no statement in the next line*/
        System.out.print("Sentence C");
        System.out.print("Sentence D");
    }
}