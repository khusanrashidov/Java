public class ModulusInJava {
    public static void main(String[] args) {
        System.out.println(12 + 4);
        System.out.println(12 - 4);
        System.out.println(12 / 4);
        System.out.println(12 * 4);
        System.out.println(12 % 4);
        System.out.println(1 % 8);
        System.out.println(12 % 8);
        System.out.println(1 % 0.8);
        System.out.println(1 % 0.6);
    }
}