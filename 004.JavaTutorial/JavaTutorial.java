public class JavaTutorial {
    public static void main (String [] args) {
        //comment
        //Escaping characters
        System.out.println("The \"fish\" was blue"); //using back slash to print the double quotes out
        System.out.println("The fish was blue\\"); //using back slash to print out the backs lash
        System.out.println("These are double quotes \"\"");
        System.out.println("This is a backslash \\");
        System.out.println("What do you mean \"funky\"?");
        System.out.println("The file is located at ~\\Desktop\\FunnyCatPics");
        System.out.println("This is a tab \t!");
        System.out.println("This \n is \n on \n multiple lines");
    }
}